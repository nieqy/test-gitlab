package com.funshion.activity.annualreport.entity;

public class AnnualReportInfo {

	private Integer id;

	private String mac;

	private String annualInfo;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getAnnualInfo() {
		return annualInfo;
	}

	public void setAnnualInfo(String annualInfo) {
		this.annualInfo = annualInfo;
	}

}
